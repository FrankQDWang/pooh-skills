def analyze_repo(repo: Path) -> dict[str, Any]:
    findings: list[Finding] = []
    seen: set[tuple[str, str, int, str]] = set()

    languages: set[str] = set()
    frameworks: set[str] = set()
    protocols: set[str] = set()

    relevant_files: set[str] = set()
    openapi_files: set[str] = set()
    asyncapi_files: set[str] = set()
    catalog_files: set[str] = set()
    generated_files: set[str] = set()
    handler_files: set[str] = set()
    docs_files: set[str] = set()

    files_scanned = 0
    core_field_hits: Counter[str] = Counter()
    extension_hits: Counter[str] = Counter()
    doc_claims = 0
    code_usage_found = False
    global_handler_found = False
    ci_guard_found = False
    async_structured_found = False
    problem_media_found = False

    for path in iter_files(repo):
        files_scanned += 1
        rel_path = rel(path, repo)
        suffix = path.suffix.lower()
        text = read_text(path)
        if not text:
            continue
        lines = text.splitlines()
        lower_path = rel_path.lower()

        if suffix in {".py", ".pyi"}:
            languages.add("python")
        elif suffix in {".ts", ".tsx"}:
            languages.add("typescript")
        elif suffix in {".js", ".jsx", ".mjs", ".cjs"}:
            languages.add("javascript")

        for framework, pattern in FRAMEWORK_RE.items():
            if pattern.search(text):
                frameworks.add(framework)

        if suffix in DOC_EXTS:
            docs_files.add(rel_path)
            if DOC_CLAIM_RE.search(text):
                doc_claims += 1

        if OPENAPI_RE.search(rel_path) or (suffix in {".yaml", ".yml", ".json"} and OPENAPI_RE.search(text[:10000])):
            openapi_files.add(rel_path)
            relevant_files.add(rel_path)
            protocols.add("http")

        if ASYNCAPI_RE.search(rel_path) or (suffix in {".yaml", ".yml", ".json"} and ASYNCAPI_RE.search(text[:10000])):
            asyncapi_files.add(rel_path)
            relevant_files.add(rel_path)
            protocols.add("async")

        if PATH_SURFACE_RE["catalog"].search(rel_path):
            catalog_files.add(rel_path)
            relevant_files.add(rel_path)

        if PATH_SURFACE_RE["generated"].search(rel_path) and CODEGEN_HINT_RE.search(text[:12000]):
            generated_files.add(rel_path)
            relevant_files.add(rel_path)

        if suffix in CODE_EXTS and PATH_SURFACE_RE["handler"].search(rel_path):
            handler_files.add(rel_path)
            relevant_files.add(rel_path)
            protocols.add("http")

        if PATH_SURFACE_RE["schema"].search(rel_path) or PROBLEM_DETAILS_RE.search(text[:12000]):
            relevant_files.add(rel_path)

        if PROBLEM_MEDIA_RE.search(text):
            problem_media_found = True
        for key, pattern in CORE_FIELD_RE.items():
            if pattern.search(text):
                core_field_hits[key] += 1
        for key, pattern in EXTENSION_FIELD_RE.items():
            if pattern.search(text):
                extension_hits[key] += 1
        if EXTENSION_FIELD_RE["code"].search(text):
            code_usage_found = True
        if ASYNC_STRUCTURED_RE.search(text):
            async_structured_found = True
        if any(pattern.search(text) for pattern in GLOBAL_HANDLER_RE):
            global_handler_found = True
        if ".github/workflows/" in lower_path or lower_path.endswith(("pre-commit-config.yaml", "pre-commit-config.yml")):
            if CI_GUARD_RE.search(text):
                ci_guard_found = True

        if suffix in CODE_EXTS and "/tests/" not in f"/{lower_path}" and "test_" not in path.name.lower():
            text_branch = first_match(lines, TEXT_BRANCH_RE)
            if text_branch:
                line_no, snippet = text_branch
                add_finding(
                    findings,
                    seen,
                    make_finding(
                        "text-branching",
                        "critical",
                        "high",
                        rel_path,
                        "Business control flow depends on message text",
                        rel_path,
                        line_no,
                        snippet,
                        "replace-text-branching",
                        "Replace message-text branching with stable `code` handling and one shared error contract.",
                        [
                            "Prove branch logic uses stable `code` values instead of `detail`, `message`, or `title`.",
                            "Add tests showing copy changes do not break control flow.",
                        ],
                        "block-now",
                    ),
                )

            if OUTWARD_HINT_RE.search(text):
                leakage_match = first_match(lines, LEAKAGE_RE)
                if leakage_match:
                    line_no, snippet = leakage_match
                    add_finding(
                        findings,
                        seen,
                        make_finding(
                            "leakage-risk",
                            "critical",
                            "high",
                            rel_path,
                            "Outward-facing error path appears to leak internal detail",
                            rel_path,
                            line_no,
                            snippet,
                            "strip-internals",
                            "Normalize outward error responses at the boundary and keep stack/database detail in internal logs only.",
                            [
                                "Verify no public error response exposes traceback, SQL detail, or raw exception text.",
                                "Add one boundary test that pins the public error object for representative failures.",
                            ],
                            "block-now",
                        ),
                    )

            numeric_code = first_match(lines, [NUMERIC_CODE_RE])
            if numeric_code:
                line_no, snippet = numeric_code
                add_finding(
                    findings,
                    seen,
                    make_finding(
                        "numeric-or-vague-code",
                        "high",
                        "medium",
                        rel_path,
                        "Business identity appears to rely on numeric error codes",
                        rel_path,
                        line_no,
                        snippet,
                        "stabilize-codes",
                        "Replace numeric or weak public codes with stable, documented string keys backed by one catalog.",
                        [
                            "Verify public clients branch on stable string codes, not numeric trivia.",
                            "Add uniqueness checks for public `code` values in the source of truth.",
                        ],
                        "fix-before-release",
                    ),
                )

    public_surfaces = bool(openapi_files or asyncapi_files or handler_files)
    typed_languages = languages & {"python", "typescript", "javascript"}
    multi_language_public = len(typed_languages) >= 2
    core_problem_count = sum(int(core_field_hits[key] > 0) for key in CORE_FIELD_RE)
    extension_count = sum(int(extension_hits[key] > 0) for key in ("code", "trace_id", "timestamp", "service"))
    coherent_problem_shape = (
        public_surfaces
        and problem_media_found
        and core_problem_count >= 3
        and code_usage_found
        and global_handler_found
    )

    if public_surfaces and (not problem_media_found and core_problem_count < 3):
        add_finding(
            findings,
            seen,
            make_finding(
                "universal-problem-gap",
                "high",
                "medium",
                "repo",
                "No coherent shared Problem Details-style public error shape is visible",
                ".",
                1,
                "Public API or handler surfaces exist, but shared Problem Details evidence is weak or missing.",
                "extract-shared-problem-schema",
                "Extract one shared public error schema and route outward responses through it.",
                [
                    "Verify representative HTTP errors share one object shape.",
                    "Verify the same top-level fields appear in contracts and runtime handlers.",
                ],
                "fix-before-release",
            ),
        )

    if public_surfaces and extension_hits["code"] == 0:
        add_finding(
            findings,
            seen,
            make_finding(
                "field-pattern-gap",
                "high",
                "medium",
                "repo",
                "Public error shape lacks stable structured business codes",
                ".",
                1,
                "Problem-shape or outward error evidence exists, but stable top-level `code` evidence is missing.",
                "introduce-structured-code",
                "Add one stable top-level `code` field and migrate clients away from message parsing.",
                [
                    "Verify public error examples and runtime handlers both include `code`.",
                    "Add tests showing localization or copy changes do not change branch behavior.",
                ],
                "fix-before-release",
            ),
        )

    if public_surfaces and extension_count < 4:
        add_finding(
            findings,
            seen,
            make_finding(
                "traceability-gap",
                "medium",
                "medium",
                "repo",
                "Public error responses do not show consistent traceability metadata",
                ".",
                1,
                "Visible public error-contract evidence is missing some of `trace_id`, `timestamp`, or `service`.",
                "inject-traceability",
                "Inject traceability metadata consistently at the public error boundary instead of ad-hoc per handler.",
                [
                    "Verify public errors carry `trace_id`, `timestamp`, and `service` consistently.",
                ],
                "fix-next",
            ),
        )

    if openapi_files and not problem_media_found:
        add_finding(
            findings,
            seen,
            make_finding(
                "http-mapping-gap",
                "high",
                "medium",
                "http",
                "OpenAPI surface lacks clear `application/problem+json` evidence",
                ".",
                1,
                "OpenAPI-like surfaces exist, but no `application/problem+json` evidence was found.",
                "align-http-contract",
                "Align public HTTP error responses and contracts on one `application/problem+json` shape.",
                [
                    "Verify OpenAPI documents the same error media type that runtime handlers emit.",
                ],
                "fix-before-release",
            ),
        )

    if asyncapi_files and not async_structured_found:
        add_finding(
            findings,
            seen,
            make_finding(
                "async-contract-gap",
                "high",
                "medium",
                "async",
                "Async error payload discipline is not clearly modeled",
                ".",
                1,
                "AsyncAPI or event surfaces were found, but structured async error-payload evidence is weak.",
                "model-async-errors",
                "Model async error payloads as explicit structured contracts instead of plain strings or implicit failure semantics.",
                [
                    "Verify async error messages carry structured payload fields such as `code`, `type`, and `detail`.",
                ],
                "fix-before-release",
            ),
        )

    if handler_files and not global_handler_found:
        add_finding(
            findings,
            seen,
            make_finding(
                "no-global-handler",
                "high",
                "medium",
                "runtime",
                "No credible global error translation layer is visible",
                ".",
                1,
                "Handler surfaces were found, but no clear global exception boundary was detected.",
                "add-boundary-handler",
                "Introduce one outward-facing error boundary that normalizes exceptions into the shared public contract.",
                [
                    "Verify representative failures are translated by one boundary layer instead of ad-hoc handler logic.",
                ],
                "fix-before-release",
            ),
        )

    if public_surfaces and code_usage_found and not catalog_files:
        ssot_severity = "high" if multi_language_public or asyncapi_files else "medium"
        ssot_merge_gate = "fix-before-release" if ssot_severity == "high" else "fix-next"
        ssot_title = (
            "Public error codes do not appear to have one dedicated source of truth"
            if ssot_severity == "high"
            else "Structured public error codes exist, but the source of truth is still implicit"
        )
        ssot_evidence = (
            "Structured code usage was detected, but no dedicated error catalog surface was found across multiple public surfaces."
            if ssot_severity == "high"
            else "Structured code usage was detected, but no dedicated catalog surface was found. In a single-language repo with a coherent boundary this is debt, not immediate proof of drift."
        )
        add_finding(
            findings,
            seen,
            make_finding(
                "ssot-missing",
                ssot_severity,
                "medium",
                "repo",
                ssot_title,
                ".",
                1,
                ssot_evidence,
                "create-catalog",
                "Create one versioned error catalog and treat it as the source of truth for public codes and types.",
                [
                    "Verify every public code is declared in one catalog.",
                    "Verify handlers and docs derive from the same catalog.",
                ],
                ssot_merge_gate,
                notes="This is a stronger blocker when multiple languages or protocols expose the same public error contract.",
            ),
        )

    if {"python", "typescript"} <= languages and not generated_files:
        add_finding(
            findings,
            seen,
            make_finding(
                "codegen-drift",
                "high",
                "medium",
                "repo",
                "Multi-language error artifacts do not appear to be generated from one source",
                ".",
                1,
                "Python and TypeScript surfaces were detected, but no generated error-artifact surface was found.",
                "restore-codegen",
                "Generate multi-language error types from the same catalog instead of maintaining parallel public code lists.",
                [
                    "Verify Python and TypeScript error artifacts are regenerated from one source of truth.",
                ],
                "fix-before-release",
            ),
        )

    if catalog_files and not ci_guard_found:
        add_finding(
            findings,
            seen,
            make_finding(
                "ci-gate-missing",
                "medium",
                "low",
                "repo",
                "Catalog or generated artifact drift is not visibly gated in CI",
                ".",
                1,
                "Catalog-like surfaces were found, but no visible CI/pre-commit guard for error-contract drift was detected.",
                "add-drift-check",
                "Add CI or review checks that fail on catalog uniqueness, schema drift, or generated-artifact staleness.",
                [
                    "Verify a PR fails when the public error catalog and generated artifacts drift apart.",
                ],
                "fix-next",
            ),
        )

    if doc_claims and public_surfaces and not problem_media_found and not global_handler_found:
        add_finding(
            findings,
            seen,
            make_finding(
                "docs-runtime-drift",
                "medium",
                "low",
                "repo",
                "Documentation claims look stronger than visible runtime error-governance evidence",
                ".",
                1,
                "Documentation mentions structured error-governance signals, but runtime evidence remains weak.",
                "close-doc-runtime-gap",
                "Either align runtime behavior with the documented contract or reduce unsupported claims in docs/examples.",
                [
                    "Verify runtime behavior matches the documented public error contract.",
                ],
                "watch",
            ),
        )

    severity_counts = Counter({"critical": 0, "high": 0, "medium": 0, "low": 0})
    for finding in findings:
        severity_counts[finding.severity] += 1

    categories = {finding.category for finding in findings}
    gate_states = [
        gate_state(
            "universal-problem-shape",
            "fail" if {"universal-problem-gap", "field-pattern-gap"} & categories else ("pass" if public_surfaces else "not-applicable"),
            "high",
            "One shared public error object is not yet evident." if {"universal-problem-gap", "field-pattern-gap"} & categories else (
                "Public error shape looks coherent enough for the bundled baseline." if public_surfaces else "No public API or event surface was detected."
            ),
        ),
        gate_state(
            "stable-business-codes",
            "fail" if {"text-branching", "numeric-or-vague-code"} & categories else (
                "watch" if "ssot-missing" in categories else ("pass" if code_usage_found or catalog_files else "watch")
            ),
            "critical" if "text-branching" in categories else "high",
            "Structured business codes are not governed durably enough." if {"text-branching", "numeric-or-vague-code"} & categories else (
                "Structured business codes exist, but the source of truth is still implicit." if "ssot-missing" in categories else "No top-level stable-code failure was detected by the bundled baseline."
            ),
        ),
        gate_state(
            "protocol-alignment",
            "fail" if {"http-mapping-gap", "async-contract-gap"} & categories else ("pass" if openapi_files or asyncapi_files else "not-applicable"),
            "high",
            "Protocol contracts drift from the desired shared error model." if {"http-mapping-gap", "async-contract-gap"} & categories else (
                "Visible protocol surfaces do not show a high-signal alignment failure." if openapi_files or asyncapi_files else "No protocol contract surface was detected."
            ),
        ),
        gate_state(
            "boundary-safety",
            "fail" if {"leakage-risk", "no-global-handler"} & categories else ("watch" if "traceability-gap" in categories else ("pass" if public_surfaces else "not-applicable")),
            "critical" if "leakage-risk" in categories else "high",
            "Outward-facing boundary safety is currently too weak." if {"leakage-risk", "no-global-handler"} & categories else (
                "Traceability evidence is incomplete." if "traceability-gap" in categories else (
                    "No high-signal outward boundary failure was detected." if public_surfaces else "No outward boundary surface was detected."
                )
            ),
        ),
        gate_state(
            "ssot-and-codegen",
            "fail" if "codegen-drift" in categories or ("ssot-missing" in categories and multi_language_public) else (
                "watch" if {"ssot-missing", "ci-gate-missing"} & categories else "pass"
            ),
            "high",
            "Catalog/codegen governance is currently too weak." if "codegen-drift" in categories or ("ssot-missing" in categories and multi_language_public) else (
                "A dedicated source of truth or drift guard is still missing." if {"ssot-missing", "ci-gate-missing"} & categories else "No high-signal SSOT/codegen failure was detected."
            ),
        ),
    ]

    if not public_surfaces and not catalog_files and not generated_files:
        overall_verdict = "not-applicable"
        summary_line = "No public error-contract, schema, catalog, or generated-type surface was detected for this audit."
    elif severity_counts["critical"] > 0 or severity_counts["high"] >= 3:
        overall_verdict = "blocked"
        summary_line = "The repo shows high-signal public error-contract drift that should block trust in the current outward error surface."
    elif severity_counts["high"] > 0 or any(gate.status == "fail" for gate in gate_states):
        overall_verdict = "partial"
        summary_line = "The repo has meaningful error-governance gaps: some structure exists, but the public contract is not yet trustworthy end to end."
    elif coherent_problem_shape and "ssot-missing" in categories and len(findings) == 1:
        overall_verdict = "watch"
        summary_line = "The public error surface looks coherent, but the source of truth for codes is still implicit and should be centralized before the contract grows."
    elif findings:
        overall_verdict = "watch"
        summary_line = "The repo shows moderate error-governance debt, but the bundled baseline did not find a top-level blocker."
    else:
        overall_verdict = "healthy"
        summary_line = "No bundled high-signal error-governance drift was found."

    top_actions = summarize_actions(findings)
    summary = {
        "schema_version": SCHEMA_VERSION,
        "skill": SKILL,
        "generated_at": utc_now(),
        "repo_root": str(repo.resolve()),
        "overall_verdict": overall_verdict,
        "summary_line": summary_line,
        "language_profile": {
            "languages": sorted(languages),
            "frameworks": sorted(frameworks),
            "protocols": sorted(protocols),
        },
        "coverage": {
            "files_scanned": files_scanned,
            "relevant_files": len(relevant_files),
            "openapi_surfaces": len(openapi_files),
            "asyncapi_surfaces": len(asyncapi_files),
            "catalogs_found": len(catalog_files),
            "generated_type_surfaces": len(generated_files),
        },
        "severity_counts": {key: int(severity_counts[key]) for key in ("critical", "high", "medium", "low")},
        "gate_states": [gate.to_dict() for gate in gate_states],
        "scan_blockers": [],
        "findings": [],
        "top_actions": top_actions,
        "dependency_status": "ready",
        "bootstrap_actions": [],
        "dependency_failures": [],
    }
    for idx, finding in enumerate(findings, start=1):
        finding.id = f"egh-{idx:03d}"
        summary["findings"].append(finding.to_dict())
    return summary


def main() -> int:
    args = parse_args()
    repo = Path(args.repo).resolve()
    out_path = Path(args.out).resolve()
    if not repo.is_dir():
        raise SystemExit(f"Repository root does not exist or is not a directory: {repo}")

    summary = analyze_repo(repo)
    report = render_human_report(summary)
    brief = render_agent_brief(summary)

    out_path.parent.mkdir(parents=True, exist_ok=True)
    out_path.write_text(json.dumps(summary, indent=2, ensure_ascii=False) + "\n", encoding="utf-8")
    report_path = Path(args.report_out).resolve() if args.report_out else out_path.with_name("error-governance-report.md")
    brief_path = Path(args.agent_brief_out).resolve() if args.agent_brief_out else out_path.with_name("error-governance-agent-brief.md")
    report_path.parent.mkdir(parents=True, exist_ok=True)
    brief_path.parent.mkdir(parents=True, exist_ok=True)
    report_path.write_text(report, encoding="utf-8")
    brief_path.write_text(brief, encoding="utf-8")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
