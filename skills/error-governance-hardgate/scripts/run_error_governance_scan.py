@dataclass
class Finding:
    id: str
    category: str
    severity: str
    confidence: str
    scope: str
    title: str
    path: str
    line: int
    evidence_summary: str
    decision: str
    recommended_change_shape: str
    validation_checks: list[str]
    merge_gate: str
    notes: str = ""

    def to_dict(self) -> dict[str, Any]:
        return asdict(self)


@dataclass
class GateState:
    name: str
    status: str
    severity_bias: str
    summary: str

    def to_dict(self) -> dict[str, str]:
        return asdict(self)


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Scan a repo for error-governance drift.")
    parser.add_argument("--repo", required=True)
    parser.add_argument("--out", required=True)
    parser.add_argument("--report-out", default=None)
    parser.add_argument("--agent-brief-out", default=None)
    return parser.parse_args()


def utc_now() -> str:
    return datetime.now(timezone.utc).isoformat()


def iter_files(root: Path) -> Iterable[Path]:
    for dirpath, dirnames, filenames in os.walk(root):
        dirnames[:] = [item for item in dirnames if item not in SKIP_DIRS]
        for filename in filenames:
            path = Path(dirpath) / filename
            if path.suffix.lower() in TEXT_EXTS:
                yield path


def read_text(path: Path) -> str:
    try:
        return path.read_text(encoding="utf-8")
    except UnicodeDecodeError:
        return path.read_text(encoding="utf-8", errors="ignore")
    except OSError:
        return ""


def rel(path: Path, repo: Path) -> str:
    return path.relative_to(repo).as_posix()


def line_snippet(lines: list[str], line_no: int) -> str:
    if 1 <= line_no <= len(lines):
        return lines[line_no - 1].strip()[:240]
    return ""


def first_match(lines: list[str], patterns: Iterable[re.Pattern[str]]) -> tuple[int, str] | None:
    for idx, line in enumerate(lines, start=1):
        if any(pattern.search(line) for pattern in patterns):
            return idx, line.strip()[:240]
    return None


def has_any(text: str, patterns: Iterable[re.Pattern[str]]) -> bool:
    return any(pattern.search(text) for pattern in patterns)


def add_finding(findings: list[Finding], seen: set[tuple[str, str, int, str]], finding: Finding) -> None:
    key = (finding.category, finding.path, finding.line, finding.title)
    if key in seen:
        return
    seen.add(key)
    findings.append(finding)


def make_finding(
    category: str,
    severity: str,
    confidence: str,
    scope: str,
    title: str,
    path: str,
    line: int,
    evidence_summary: str,
    decision: str,
    recommended_change_shape: str,
    validation_checks: list[str],
    merge_gate: str,
    notes: str = "",
) -> Finding:
    return Finding(
        id="",
        category=category,
        severity=severity,
        confidence=confidence,
        scope=scope,
        title=title,
        path=path,
        line=line,
        evidence_summary=evidence_summary,
        decision=decision,
        recommended_change_shape=recommended_change_shape,
        validation_checks=validation_checks,
        merge_gate=merge_gate,
        notes=notes,
    )


def summarize_actions(findings: list[Finding]) -> list[str]:
    categories = {finding.category for finding in findings}
    actions: list[str] = []
    if {"universal-problem-gap", "field-pattern-gap", "http-mapping-gap"} & categories:
        actions.append("Extract one shared public error schema and align HTTP responses on `application/problem+json` plus stable top-level fields.")
    if {"text-branching", "numeric-or-vague-code", "ssot-missing"} & categories:
        actions.append("Replace message-text branching with one versioned error-code catalog and structured `code` handling.")
    if {"leakage-risk", "no-global-handler", "traceability-gap"} & categories:
        actions.append("Add one outward-facing boundary translator that strips internals and injects traceability fields consistently.")
    if {"codegen-drift", "ci-gate-missing"} & categories:
        actions.append("Generate Python / TypeScript error artifacts from the same source of truth and add drift checks in CI.")
    if {"async-contract-gap"} & categories:
        actions.append("Make async error payloads structurally explicit instead of relying on plain strings or implicit failure semantics.")
    if not actions:
        actions.append("Keep the current error contract explicit and resist reintroducing ad-hoc JSON error shapes.")
    return actions[:3]


def gate_state(name: str, status: str, severity_bias: str, summary: str) -> GateState:
    return GateState(name=name, status=status, severity_bias=severity_bias, summary=summary)


